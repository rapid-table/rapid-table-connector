package com.rapidtable.sdk.rtc4j.resource.tag;

/**
 * Tag Resource Response model
 */
public class TagResponse {
    /**
     * Tag ID
     */
    private String id;
    /**
     * Tag value
     */
    private String text;
    /**
     * Tag description
     */
    private String description;
    /**
     * Tag color
     */
    private String color;

    public TagResponse() {
    }

    public TagResponse(String id, String text, String description, String color) {
        this.id = id;
        this.text = text;
        this.description = description;
        this.color = color;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getText() {
        return text;
    }

    public void setText(String text) {
        this.text = text;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }
}
